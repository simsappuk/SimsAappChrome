package com.ebay.load.seller.ListingClasses;

public class ShippingPackageDetails {
    private Boolean shippingIrregular;
    private String shippingPackage;
    private String weightMajorValue;
    private String weightMajorUnit;
    private String weightMajorMeasurementSystem;
    private String weightMinorValue;
    private String weightMinorUnit;
    private String weightMinorMeasurementSystem;


    public Boolean getShippingIrregular() {
        return shippingIrregular;
    }

    public void setShippingIrregular(Boolean shippingIrregular) {
        this.shippingIrregular = shippingIrregular;
    }

    public String getShippingPackage() {
        return shippingPackage;
    }

    public void setShippingPackage(String shippingPackage) {
        this.shippingPackage = shippingPackage;
    }

    public String getWeightMajorValue() {
        return weightMajorValue;
    }

    public void setWeightMajorValue(String weightMajorValue) {
        this.weightMajorValue = weightMajorValue;
    }

    public String getWeightMajorUnit() {
        return weightMajorUnit;
    }

    public void setWeightMajorUnit(String weightMajorUnit) {
        this.weightMajorUnit = weightMajorUnit;
    }

    public String getWeightMajorMeasurementSystem() {
        return weightMajorMeasurementSystem;
    }

    public void setWeightMajorMeasurementSystem(String weightMajorMeasurementSystem) {
        this.weightMajorMeasurementSystem = weightMajorMeasurementSystem;
    }

    public String getWeightMinorValue() {
        return weightMinorValue;
    }

    public void setWeightMinorValue(String weightMinorValue) {
        this.weightMinorValue = weightMinorValue;
    }

    public String getWeightMinorUnit() {
        return weightMinorUnit;
    }

    public void setWeightMinorUnit(String weightMinorUnit) {
        this.weightMinorUnit = weightMinorUnit;
    }

    public String getWeightMinorMeasurementSystem() {
        return weightMinorMeasurementSystem;
    }

    public void setWeightMinorMeasurementSystem(String weightMinorMeasurementSystem) {
        this.weightMinorMeasurementSystem = weightMinorMeasurementSystem;
    }
}
